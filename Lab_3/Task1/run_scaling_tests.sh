#!/bin/bash

# Путь к скомпилированному исполняемому файлу
EXECUTABLE="./build/matrix_mult"

# Проверка наличия исполняемого файла
if [ ! -f "$EXECUTABLE" ]; then
    echo "Ошибка: Исполняемый файл $EXECUTABLE не найден."
    echo "Сначала соберите проект: mkdir -p build && cd build && cmake .. && make"
    exit 1
fi

# Список потоков для тестирования
THREADS=(1 2 4 7 8 16 20 40)

# Файл для сохранения результатов
RESULT_FILE="scaling_results.txt"
echo "Threads,Initialization_Time,Multiplication_Time" > $RESULT_FILE

echo "Начинаю замеры производительности..."
echo "--------------------------------------"

for T in "${THREADS[@]}"
do
    echo "Запуск на $T потоках..."
    
    # Для того чтобы программа использовала нужное число потоков, 
    # мы можем либо изменить код, либо (если код использует hardware_concurrency) 
    # имитировать ограничение через taskset или изменить логику в коде.
    # В данном случае, так как код внутри main.cpp сам определяет число потоков через hardware_concurrency,
    # нам нужно либо модифицировать main.cpp для приема аргумента, либо использовать скрипт для подмены.
    
    # Рекомендуемый вариант: я обновлю main.cpp, чтобы он принимал число потоков аргументом.
    # А пока запускаем как есть и выводим результат.
    
    OUTPUT=$($EXECUTABLE $T)
    echo "$OUTPUT"
    echo "--------------------------------------"
done

echo "Тестирование завершено."
